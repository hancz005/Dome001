public class IncreaseDecrease {
    public static void main(String[] args) {
        int a = 1;
        // System.out.println("a ++=" + a++);  //++ = 自增
        System.out.println("a=" + a);

        int b = 10;
        System.out.println("b--=" + b--);  //-- = 自减
        System.out.println("b=" + b);
    }
}
